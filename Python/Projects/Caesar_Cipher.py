def caesar(text,shift,direction):
    end_text=""
    if direction=="decode" :
        shift*=-1
    for c in text :
        if c in lower_charset :
            position = lower_charset.index(c)
            new_position = position + shift
            end_text += lower_charset[new_position]
        elif c in upper_charset:
            position = upper_charset.index(c)
            new_position = position + shift
            end_text += upper_charset[new_position]
        else :
            end_text+=c
    print(f"Here's the {direction}d result : {end_text}")


